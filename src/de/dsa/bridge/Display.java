package de.dsa.bridge;

public class Display {
    DisplayImpl impl;

    Display(DisplayImpl display) {
        impl = display;
    }

    public void open() {
        impl.rawOpen();
    }

    public void print() {
        impl.rawPrint();
    }

    public void close() {
        impl.rawClose();
    }

    public final void display() {
        open();
        print();
        close();
    }
}
